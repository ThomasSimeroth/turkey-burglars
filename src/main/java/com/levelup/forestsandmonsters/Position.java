import java.awt.Point;

public class Position {

    private int x;
    private int y;

    public int getX() {
        return x;
    }

    public int getY() {
        return y;
    }

    public Position(int xCoordinates, int YCoordinates) {
        this.x = xCoordinates;
        this.y = YCoordinates;
    }
    
}
